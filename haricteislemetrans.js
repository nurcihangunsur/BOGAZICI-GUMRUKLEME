      function translateExternalProcessingGuide(lang) {
        try {
          // Belirtilen dilde çevirileri al
          var translations = language14Translations[lang];
      
          // Başlık çevirisi
          document.querySelector('.titleHaric').innerText = translations['externalProcessingGuide'];
      
          // Soru ve cevapları çevir
          var questionAnswers = document.querySelectorAll('.questionq6');
          questionAnswers.forEach(function (item, index) {
            var questionKey = 'question' + (index + 1);
            var answerKey = 'answer' + (index + 1);
            item.querySelector('p').innerText = translations[questionKey];
            item.querySelector('.answeeer6').innerText = translations[answerKey];
          });
      
        } catch (error) {
          console.error("Haricî İşleme Rehberi çevirme hatası:", error);
        }
      }
        function translateFooter(lang) {
          try {
            var translations = footerSectionTranslations[lang];
        
            // Şirket Bilgisi Çevirisi
            document.querySelector('.company-info h2').textContent = translations['companyInfo']['companyName'];
            document.querySelector('.company-info p:nth-child(2)').textContent = translations['companyInfo']['address'];
            document.querySelector('.company-info p:nth-child(3)').textContent = translations['companyInfo']['telephone'];
            document.querySelector('.company-info p:nth-child(4)').textContent = translations['companyInfo']['email'];
        
            // Hızlı Menü Çevirisi
            document.querySelector('.quick-menu h2').textContent = translations['quickMenu']['title'];
            var quickMenuItems = translations['quickMenu']['menuItems'];
            var quickMenuList = document.querySelectorAll('.quick-menu ul li');
            quickMenuList.forEach(function (item, index) {
              item.innerHTML = `<i class="fas fa-chevron-right"></i>${quickMenuItems[index]['text']}`;
              item.setAttribute('onclick', `window.location.href='${quickMenuItems[index]['link']}'`);
            });
        
            // Hizmetlerimiz Çevirisi
            document.querySelector('.services3 h2').textContent = translations['ourServices']['title'];
            var servicesItems = translations['ourServices']['serviceItems'];
            var servicesList = document.querySelectorAll('.services3 ul li');
            servicesList.forEach(function (item, index) {
              item.innerHTML = `<i class="fas fa-chevron-right"></i>${servicesItems[index]['text']}`;
              item.setAttribute('onclick', `window.location.href='${servicesItems[index]['link']}'`);
            });
        
            // Faydalı Bilgiler Çevirisi
            document.querySelector('.useful-info2 h2').textContent = translations['usefulInfo']['title'];
            var usefulInfoItems = translations['usefulInfo']['serviceItems'];
            var usefulInfoList = document.querySelectorAll('.useful-info2 ul li');
            usefulInfoList.forEach(function (item, index) {
              if (usefulInfoItems[index]['external']) {
                var linkElement = item.querySelector('a');
                linkElement.innerHTML = `<i class="fas fa-chevron-right"></i>${usefulInfoItems[index]['text']}`;
                linkElement.setAttribute('href', usefulInfoItems[index]['link']);
                linkElement.setAttribute('target', '_blank');
              } else {
                item.innerHTML = `<i class="fas fa-chevron-right"></i>${usefulInfoItems[index]['text']}`;
                item.setAttribute('onclick', `window.location.href='${usefulInfoItems[index]['link']}'`);
              }
            });
        
          } catch (error) {
            console.error("Footer çevirme hatası:", error);
          }
        }
      function updateContent(lang) {
        var emailElement = document.getElementById('email2');
        var phoneElement = document.getElementById('phone');
        var locationElement = document.getElementById('location');
    
        if (translations[lang] && translations[lang]['email2']) {
            emailElement.innerHTML = '<i class="far fa-envelope"></i> <a href="mailto:info@example.com">' + translations[lang]['email2'] + '</a>';
        }
    
        if (translations[lang] && translations[lang]['phone']) {
            phoneElement.innerHTML = '<i class="fas fa-phone"></i> <a href="tel:+901234567890">' + translations[lang]['phone'] + '</a>';
        }
    
        if (translations[lang] && translations[lang]['location']) {
            locationElement.innerHTML = '<i class="fas fa-map-marker-alt"></i>' + translations[lang]['location'];
        }
    }
      
      