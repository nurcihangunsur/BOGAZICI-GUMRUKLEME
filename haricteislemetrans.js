      function checkAndSetLanguage() {
        // Sayfanın ilk yüklenmesinde dil bilgisini localStorage'dan al
        // Eğer dil bilgisi yoksa varsayılan olarak 'en' kullan
        var selectedLanguage = localStorage.getItem('language') || 'en';
    
        // Sayfayı çevir
        translatePage(selectedLanguage);
    
        // Slider içeriğini çevir
        translateSliderContent(selectedLanguage);
    }
    
